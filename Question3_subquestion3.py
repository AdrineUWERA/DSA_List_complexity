# this is a program to sort integers
# A method that will receive the list of integers and perform the sort using sort function.
# then it will return the sorted list.
def sort_list(list2):
    list2.sort()
    return list2


print(sort_list([1, 2, 5, 6, 3, 4])) # Passing the given list in the sort method and printing the sorted list
